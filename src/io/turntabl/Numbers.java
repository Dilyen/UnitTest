package io.turntabl;

public class Numbers {
    public static int sum(int a, int b){
        return a + b;
    }

    public static boolean isPositive(int a){
        if(a <= 0){
            return false;
        }else {
            return true;
        }
    }
    public static boolean isNegative(int a){
        if(a <= 0){
            return false;
        }else {
            return true;
        }

    }

}
